class Raca:
    def __init__(self, id = None, nome = None, tipo_de_criatura = None, tamanho = None, velocidade = None, traco_especiais = None, descricao = None):
        self.id = id
        self.nome = nome
        self.tipo_de_criatura = tipo_de_criatura
        self.tamanho = tamanho
        self.velocidade = velocidade
        self.traco_especiais = traco_especiais
        self.descricao = descricao